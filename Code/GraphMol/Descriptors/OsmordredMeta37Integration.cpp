// Build full 3624-feature vector for NEW XGBoost models
// Structure: Osmordred(3585) + Abraham(6) + Meta37Base(5) + MW(1) + Physics(3) + LSFER(21) + Hansen(3)
std::vector<double> buildFullFeatures(
    const std::vector<double>& osmo,      // 3585 from CalcOsmordred
    const std::vector<double>& abraham,   // 6: V,E,L,B,S,A from CalcAbrahamsV2
    double dD, double dP, double dH,      // From OLD Hansen models
    double Density, double RI,            // From OLD Density/RI models
    double MW                             // Molecular weight
) {
    std::vector<double> full;
    full.reserve(3624);
    
    // 1. Osmordred features (3585)
    for (const auto& v : osmo) full.push_back(v);
    
    // 2. Abraham V2 (6): V, E, L, B, S, A
    double V = abraham[0], E = abraham[1], L = abraham[2];
    double B = abraham[3], S = abraham[4], A = abraham[5];
    full.push_back(V); full.push_back(E); full.push_back(L);
    full.push_back(B); full.push_back(S); full.push_back(A);
    
    // 3. Meta37 base (5): dD, dP, dH, Density, RI
    full.push_back(dD); full.push_back(dP); full.push_back(dH);
    full.push_back(Density); full.push_back(RI);
    
    // 4. MW (1)
    full.push_back(MW);
    
    // 5. Physics features (3): f(n), Vm, MR
    double n2 = RI * RI;
    double f_n = (n2 + 2.0 > 0.01) ? (n2 - 1.0) / (n2 + 2.0) : 0.0;
    double Vm = (Density > 0.1) ? MW / Density : 0.0;
    double MR = Vm * f_n;
    full.push_back(f_n); full.push_back(Vm); full.push_back(MR);
    
    // 6. LSFER squares (6): V², E², L², B², S², A²
    full.push_back(V*V); full.push_back(E*E); full.push_back(L*L);
    full.push_back(B*B); full.push_back(S*S); full.push_back(A*A);
    
    // 7. LSFER products (15): all pairs
    full.push_back(V*E); full.push_back(V*L); full.push_back(V*B); full.push_back(V*S); full.push_back(V*A);
    full.push_back(E*L); full.push_back(E*B); full.push_back(E*S); full.push_back(E*A);
    full.push_back(L*B); full.push_back(L*S); full.push_back(L*A);
    full.push_back(B*S); full.push_back(B*A);
    full.push_back(S*A);
    
    // 8. Hansen products (3): dD*dP, dD*dH, dP*dH
    full.push_back(dD*dP); full.push_back(dD*dH); full.push_back(dP*dH);
    
    return full;  // Total: 3624
}

